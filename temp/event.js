function decodeToArray(string)
{
	var array=new Object();
	var stringAsItems = string.split('~~');
	stringAsItems.splice(stringAsItems.length-1,1); //remove the last empty string
	for (var index=0; index < stringAsItems.length; index = index+2)
	{
		array[stringAsItems[index]] = stringAsItems[index+1];
	}
	return array;
	
}
